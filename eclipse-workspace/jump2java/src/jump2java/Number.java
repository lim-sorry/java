package jump2java;

public class Number {
	public static void main(String[] args) {
		int a = 10;
		int b = 5;
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		
		System.out.println(7 % 3);
		System.out.println(3 % 7);
		
		int i = 0;
		int j = 10;
		i++;
		j--;
		System.out.println(i);
		System.out.println(j);
		
		int l = 0;
		System.out.println(++l);
		System.out.println(l);
	}
}
